let currentLang = 'ru';

function t(key, params) {
  const dict = I18N[currentLang] || I18N.ru;
  let str = dict[key] ?? I18N.ru[key] ?? key;
  if (params) {
    Object.keys(params).forEach((k) => {
      str = str.replace(new RegExp(`\\{${k}\\}`, 'g'), params[k]);
    });
  }
  return str;
}

function getOrderStatusLabel(status) {
  return t(`statusOrder.${status}`) || status;
}

function getDoughLabel(key) {
  return t(`dough.${key}`) || key;
}

function doughLabel(key) {
  return getDoughLabel(key);
}

function getZoneLabel(zone) {
  return t(`zone.${zone}`) || zone;
}

function applyI18n() {
  document.querySelectorAll('[data-i18n]').forEach((el) => {
    el.textContent = t(el.dataset.i18n);
  });
  document.querySelectorAll('[data-i18n-html]').forEach((el) => {
    el.innerHTML = t(el.dataset.i18nHtml);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });
  document.querySelectorAll('[data-i18n-title]').forEach((el) => {
    el.title = t(el.dataset.i18nTitle);
  });
  document.title = t('meta.title');
  document.documentElement.lang = currentLang === 'uz' ? 'uz' : currentLang;
}

function renderTicker() {
  const track = document.getElementById('tickerTrack');
  if (!track) return;
  const items = t('ticker.items').split('|');
  const doubled = [...items, ...items];
  track.innerHTML = doubled.map((x) => `<span class="ticker-item">${x}<span style="opacity:.4;padding:0 8px">·</span></span>`).join('');
}

function setLang(lang, silent){
  if (!I18N[lang]) return;
  currentLang = lang;
  try { localStorage.setItem('pm_lang', lang); } catch {}
  document.querySelectorAll('.lang-btn').forEach((b) => b.classList.toggle('on', b.dataset.lang === lang));
  applyI18n();
  renderTicker();
  if (typeof updateOpenStatus === 'function') updateOpenStatus();
  if (typeof updateCart === 'function') updateCart();
  if (typeof updateOrderState === 'function') updateOrderState();
  if (typeof updateLoyaltyUI === 'function') updateLoyaltyUI();
  if (typeof updateAuthBtn === 'function') updateAuthBtn();
  if (typeof refreshBuilderUI === 'function' && document.getElementById('builderModal')?.classList.contains('on')) refreshBuilderUI();
  if (typeof twRender === 'function' && typeof twStatus !== 'undefined' && twStatus) twRender(twStatus);
  if (!silent && typeof showToast === 'function') showToast(t('toast.lang'));
}

function initI18n() {
  try {
    const saved = localStorage.getItem('pm_lang');
    if (saved && I18N[saved]) currentLang = saved;
  } catch {}
  document.querySelectorAll('.lang-btn').forEach((b) => b.classList.toggle('on', b.dataset.lang === currentLang));
  applyI18n();
  renderTicker();
}
